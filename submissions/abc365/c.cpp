int main() {
  ll N, M;
  cin >> N >> M;
  vector<ll> A(N);
  ll sumA = 0;
  rep(i,N) {
    cin >> A[i];
    sumA += A[i];
  }
  if (sumA <= M) {
    print("infinite");
    return 0;
  }
  ll l = 0, u = 1e9+5;
  while (u-l > 1) {
    ll m = (u+l)/2;
    ll total = 0;
    rep(i,N) {
      total += min(m, A[i]);
    }
    if (total <= M)
      l = m;
    else
      u = m;
  }
  print(l);
  return 0;
}