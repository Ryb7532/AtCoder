bool pal(ll n) {
  vector<int> A;
  while (n > 0) {
    A.push_back(n%10);
    n /= 10;
  }
  int m = A.size();
  bool res = true;
  rep(i,m) {
    if (A[i] != A[m-1-i])
      res = false;
  }
  return res;
}

